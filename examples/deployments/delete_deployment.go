package examples

import (
	"fmt"
	"net/url"

	"github.com/OctopusDeploy/go-octopusdeploy/pkg/client"
)

func DeleteDeploymentExample() {
	var (
		apiKey     string = "API-YOUR_API_KEY"
		octopusURL string = "https://your_octopus_url"
		spaceID    string = "space-id"

		// deployment values
		deploymentID string = "deployment-id"
	)

	apiURL, err := url.Parse(octopusURL)
	if err != nil {
		_ = fmt.Errorf("error parsing URL for Octopus API: %v", err)
		return
	}

	client, err := client.NewClient(nil, apiURL, apiKey, spaceID)
	if err != nil {
		_ = fmt.Errorf("error creating API client: %v", err)
		return
	}

	// delete deployment
	err = client.Deployments.DeleteByID(deploymentID)
	if err != nil {
		_ = fmt.Errorf("error deleting deployment: %v", err)
		return
	}

	fmt.Printf("deployment deleted: (%s)\n", deploymentID)
}
